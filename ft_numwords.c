#include "libft.h"

int			ft_numwords(char const *s, char c)
{
	int		word;
	size_t	i;

	word = 0;
	i = -1;
	while (s[++i])
		if (s[i] != c && (s[i + 1] == c || !s[i + 1]))
			word++;
	return (word);
}
